class RomanValue1
{
	public static void main(String[] args)
 	{
 		int num=10000;
		if(num == 10)
			System.out.println("X");
		else if(num == 100)
			System.out.println("C");
		else if(num == 1000)
			System.out.println("M");
		else if(num == 10000)
			System.out.println("X bar");
		else
			System.out.println("Invalid");
	}
}